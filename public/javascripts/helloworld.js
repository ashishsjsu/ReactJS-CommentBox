//React dynamic UI and reactivity

var LikeButton = React.createClass({

	getInitialState: function(){
		return { liked: false };
	},
	handleClick: function(){
		this.setState({liked: !this.state.liked});
	},

	render: function(){
		var text = this.state.liked ? 'like' : 'haven\'t liked';
		
		return (
			<p><i className="fa fa-thumbs-o-up" onClick={this.handleClick}></i>
			You {text} this			
			</p>
		);
	}
});

React.render(
	<LikeButton />,
	document.getElementById('likeItem')
);